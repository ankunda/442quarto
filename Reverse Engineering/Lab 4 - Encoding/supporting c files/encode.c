#include <stdio.h>

/*
 * The message "Well done, analyst." XOR'd byte-by-byte with 0x3A.
 * The original string is never stored in plaintext in this binary.
 *
 * To verify: python3 -c "data=[0x6D,0x5F,0x56,0x56,0x16,0x1A,0x5E,0x55,0x54,0x5F,0x16,0x1A,0x5B,0x54,0x5B,0x56,0x43,0x49,0x4E,0x14]; print(''.join(chr(b^0x3A) for b in data))"
 */
static unsigned char encoded[] = {
    0x6D, 0x5F, 0x56, 0x56, 0x16, 0x1A, 0x5E, 0x55,
    0x54, 0x5F, 0x16, 0x1A, 0x5B, 0x54, 0x5B, 0x56,
    0x43, 0x49, 0x4E, 0x14
};

void decode(unsigned char *data, int len) {
    for (int i = 0; i < len; i++) {
        data[i] ^= 0x3A;
    }
}

int main() {
    int len = sizeof(encoded);
    decode(encoded, len);
    printf("%.*s\n", len, (char *)encoded);
    return 0;
}
